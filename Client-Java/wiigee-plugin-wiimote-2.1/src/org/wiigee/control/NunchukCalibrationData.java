package org.wiigee.control;

import java.awt.Point;

/**
 * 
 * <p>
 * @author <a href="mailto:vfritzsch@users.sourceforge.net">Volker Fritzsch</a> , javadoc upgraded by Santiago Hors Fraile
 */
public class NunchukCalibrationData {

	/**
	 * Represents the zero force of x axis of the Nunchuk.
	 */
	private int zeroForceX;
	/**
	 * Represents the zero force of y axis of the Nunchuk.
	 */
	private int zeroForceY;
	/** 
	 * Represents the zero force of z axis of the Nunchuk.
	 */
	private int zeroForceZ;
	/**
	 * Represents the gravity force of x axis of the Nunchuk.
	 */
	private int gravityForceX;
	/**
	 * Represents the gravity force of y axis of the Nunchuk.
	 */
	private int gravityForceY;
	/**
	 * Represents the gravity force of z axis of the Nunchuk.
	 */
	private int gravityForceZ;
	/**
	 * Represents the minimum analog point of the Nunchuk.
	 */
	private Point minimumAnalogPoint;
	/**
	 * Represents the maximum analog point of the Nunchuk.
	 */
	private Point maximumAnalogPoint;
	/**
	 * Represents the center analog point of the Nunchuk.
	 */
	private Point centerAnalogPoint;
	
	/**
	 * Sets all fields to their defaults values.
	 */
	public NunchukCalibrationData() {
		
	}
		
	/**
	 * Gets the zeroForceX field.
	 * @return int The current zeroForceX field.
	 */
	public int getZeroForceX() {
		return zeroForceX;
	}
	/**
	 * Sets the zeroForceX field with the given parameter.
	 * @param zeroForceX The new zeroForceX field.
	 */
	public void setZeroForceX(int zeroForceX) {
		this.zeroForceX = zeroForceX;
	}
	/**
	 * Gets the zeroForceY field.
	 * @return int The current zeroForceY field.
	 */
	public int getZeroForceY() {
		return zeroForceY;
	}
	/**
	 * Sets the zeroForceY field with the given parameter.
	 * @param zeroForceY The new zeroForceY field.
	 */
	public void setZeroForceY(int zeroForceY) {
		this.zeroForceY = zeroForceY;
	}
	/**
	 * Gets the zeroForceZ field.
	 * @return int The current zeroForceZ field.
	 */
	public int getZeroForceZ() {
		return zeroForceZ;
	}
	/**
	 * Sets the zeroForceZ field with the given parameter.
	 * @param zeroForceZ The new zeroForceZ field.
	 */
	public void setZeroForceZ(int zeroForceZ) {
		this.zeroForceZ = zeroForceZ;
	}
	/**
	 * Gets the gravityForceX field.
	 * @return int The current gravityForceX field.
	 */
	public int getGravityForceX() {
		return gravityForceX;
	}
	/**
	 * Sets the gravityForceX field with the given parameter.
	 * @param gravityForceX The new gravityForceX field.
	 */
	public void setGravityForceX(int gravityForceX) {
		this.gravityForceX = gravityForceX;
	}
	/**
	 * Gets the gravityForceY field.
	 * @return int The current gravityForceY field.
	 */
	public int getGravityForceY() {
		return gravityForceY;
	}
	/**
	 * Sets the gravityForceY field with the given parameter.
	 * @param gravityForceY The new gravityForceY field.
	 */
	public void setGravityForceY(int gravityForceY) {
		this.gravityForceY = gravityForceY;
	}
	/**
	 * Gets the gravityForceZ field.
	 * @return int The current gravityForceZ field.
	 */
	public int getGravityForceZ() {
		return gravityForceZ;
	}
	/**
	 * Sets the gravityForceZ field with the given parameter.
	 * @param gravityForceZ The new gravityForceZ field.
	 */
	public void setGravityForceZ(int gravityForceZ) {
		this.gravityForceZ = gravityForceZ;
	}
	/**
	 * Gets the minimumAnalogPoint field.
	 * @return int The current minimumAnalogPoint field.
	 */
	public Point getMinimumAnalogPoint() {
		return minimumAnalogPoint;
	}
	/**
	 * Sets the minimumAnalogPoint field with the given parameter.
	 * @param minimumAnalogPoint The new minimumAnalogPoint field.
	 */
	public void setMinimumAnalogPoint(Point minimumAnalogPoint) {
		this.minimumAnalogPoint = minimumAnalogPoint;
	}
	/**
	 * Gets the maximumAnalogPoint field.
	 * @return int The current maximumAnalogPoint field.
	 */
	public Point getMaximumAnalogPoint() {
		return maximumAnalogPoint;
	}
	/**
	 * Sets the maximumAnalogPoint field with the given parameter.
	 * @param maximumAnalogPoint The new maximumAnalogPoint field.
	 */
	public void setMaximumAnalogPoint(Point maximumAnalogPoint) {
		this.maximumAnalogPoint = maximumAnalogPoint;
	}
	/**
	 * Gets the centerAnalogPoint field.
	 * @return int The current centerAnalogPoint field.
	 */
	public Point getCenterAnalogPoint() {
		return centerAnalogPoint;
	}
	/**
	 * Sets the centerAnalogPoint field with the given parameter.
	 * @param centerAnalogPoint The new centerAnalogPoint field.
	 */
	public void setCenterAnalogPoint(Point centerAnalogPoint) {
		this.centerAnalogPoint = centerAnalogPoint;
	}

}
